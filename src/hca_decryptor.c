HcaError hca_decryptor_init_table(HcaDecryptor *decryptor) {
    if (decryptor->type == 1) {
        // decryptor->table[0] = 0;
        // decryptor->table[255] = 255;
        // for (int i = 1, v = 11; i < 255; v = (v * 13 + 11) & 0xff)
        //     if (v != 0 && v != 255)
        //         decryptor->table[i++] = v;
        memcpy(decryptor->table, table_default, 256);
        for (int i = 0; i < 65536; i++)
            decryptor->table_large[i] = (decryptor->table[i >> 8] << 8) | decryptor->table[i & 0xff];
    } else if (decryptor->type == 56) {
        uint64_t key = decryptor->key - 1;
        uint8_t t1[] = { key, key >> 8, key >> 16, key >> 24, key >> 32, key >> 40, key >> 48, key >> 56, key >> 60 };
        uint8_t t2[] = {
            t1[1], t1[1] ^ t1[6], t1[2] ^ t1[3], t1[2], t1[2] ^ t1[1], t1[3] ^ t1[4], t1[3], t1[3] ^ t1[2],
            t1[4] ^ t1[5], t1[4], t1[4] ^ t1[3], t1[5] ^ t1[6], t1[5], t1[5] ^ t1[4], t1[6] ^ t1[1], t1[6]
        };
        uint8_t t3[256];
        for (int i = 0, k = 0, m1 = (t1[0] & 1) << 3 | 5, a1 = (t1[0] & 0xe) << 4 | 0x10, v1 = t1[0] & 0xf0; i < 16; i++) {
            v1 = (v1 * m1 + a1) & 0xf0;
            for (int j = 0, m2 = (t2[i] & 1) << 3 | 5, a2 = (t2[i] & 0xe) | 1, v2 = t2[i] >> 4; j < 16; j++, k++)
                t3[k] = v1 | (v2 = (v2 * m2 + a2) & 0xf);
        }
        decryptor->table[0] = 0;
        decryptor->table[255] = 255;
        for (int i = 1, v = 17, a = t3[v]; i < 255; a = t3[v = (v + 17) & 0xff])
            if (a != 0 && a != 255)
                decryptor->table[i++] = a;
        for (int i = 0; i < 65536; i++)
            decryptor->table_large[i] = (decryptor->table[i >> 8] << 8) | decryptor->table[i & 0xff];
    } else if (decryptor->type) {
        return kHcaInvalidEnryptionType;
    }
    return kHcaSuccess;
}

HcaError hca_decryptor_decrypt(const HcaDecryptor *decryptor, uint8_t *data, const size_t len) {
    if (decryptor->type == 0)
        return kHcaSuccess;
    uint32_t *data32 = (uint32_t*)data;
    for (size_t i = 0; i < len / 4; i++)
        data32[i] = decryptor->table_large[data32[i] >> 16] << 16 | decryptor->table_large[data32[i] & 0xffff];
    for (size_t i = len & ~3; i < len; i++)
        data[i] = decryptor->table[data[i]];
    return kHcaSuccess;
}
